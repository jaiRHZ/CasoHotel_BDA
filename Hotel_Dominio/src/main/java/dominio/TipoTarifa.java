
package dominio;

/**
 * Enumeración TipoTarifa que representa los diferentes tipos de tarifas en un sistema de gestión hotelera.
 * Proporciona tres niveles de clasificación de tarifas: ESTANDAR, PREMIUM y VIP.
 * Cada tipo de tarifa puede estar asociado a diferentes niveles de servicios y precios en el contexto hotelero.
 * 
 * @author Jairo G. Rodríguez Hernández 00000213248
 */
public enum TipoTarifa {
    ESTANDAR,   // Representa una tarifa estándar con servicios básicos
    PREMIUM,    // Representa una tarifa premium con servicios mejorados
    VIP         // Representa una tarifa VIP con servicios de máxima calidad
}

